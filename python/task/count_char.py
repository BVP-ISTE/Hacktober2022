# Count occurrence of a character in a Python string

count = 0
test_str = "The Udacity Udacity" 
for i in test_str:
    if i == 'e':
        count = count + 1
  
# printing result 
print ("Count of e in The Udacity Udacity is : "
                            +  str(count))




